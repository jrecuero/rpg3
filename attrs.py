#------------------------------------------------------------------------------
class Attrs (object):
    """
    """
    #--------------------------------------------------------------------------
    @staticmethod
    def getAttrs():
        """
        """
        return (DAMAGE, DEFENSE, MONEY, HEALTH, POWER)

    #--------------------------------------------------------------------------
    @staticmethod
    def createAttrsDict():
        """
        """
        return dict(zip(Attrs.getAttrs(), (0, ) * len(Attrs.getAttrs())))

    #--------------------------------------------------------------------------
    def __init__(self):
        """ Initialize Attrs instance
        """
        self.logger   = loggerator.getLoggerator('attrs')
        for attr in Attrs.getAttrs():
            setattr(self, attr, 0)


